function vantagenspar(){   
    let maisVantagens = document.getElementById("vantagens-parceiro");
    let btnVantagens = document.getElementById("btnSaibaMaisPar");

    if(maisVantagens.style.display === "inline") {
        maisVantagens.style.display = "none";
        btnVantagens.innerHTML = "Saiba mais";
        btnVantagens.style.backgroundColor = "black";
    }else{
        maisVantagens.style.display = "inline";
        btnVantagens.innerHTML = "Minimizar";
        btnVantagens.style.backgroundColor = "#e1b55e";
    }
}

